"""**Building agents**

**description agent**
"""

def get_description(disease):
    row = desc[desc["Disease"] == disease]
    if not row.empty:
        return row["Description"].values[0]
    return "No description available"

"""**Precuation Agent**"""

def get_precautions(disease):
    row = prec[prec["Disease"] == disease]
    if not row.empty:
        return row.values[0][1:]
    return []

"""**Severity Agent**"""

severity_dict = dict(zip(sev["Symptom"], sev["weight"]))

def calculate_severity(symptoms):
    score = sum([severity_dict.get(s, 0) for s in symptoms])

    if score > 15:
        return "HIGH RISK ⚠️"
    elif score > 5:
        return "MEDIUM RISK"
    return "LOW RISK"



