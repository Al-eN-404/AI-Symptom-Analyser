def normalize_input(text):
    text = text.lower()
    normalized = []
    for symptom, synonyms in SYMPTOM_SYNONYMS.items():
        for phrase in synonyms:
            if phrase in text:
                normalized.append(symptom)  # adds "high_fever" — matches training data!
    normalized.append(text)
    return " ".join(normalized)

