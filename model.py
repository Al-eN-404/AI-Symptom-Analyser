import pandas as pd
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.ensemble import RandomForestClassifier
from sklearn.preprocessing import LabelEncoder
from sklearn.linear_model import LogisticRegression



"""**load all files**"""

# Update paths if needed
df = pd.read_csv("dataset.csv")
desc = pd.read_csv("symptom_Description.csv")
prec = pd.read_csv("symptom_precaution.csv")
sev = pd.read_csv("Symptom-severity.csv")

"""**preprocess the main dataset**"""

def combine_symptoms(row):
    symptoms = []
    for col in df.columns[1:]:  # skip Disease column
        if pd.notna(row[col]):
            symptoms.append(str(row[col]))
    return " ".join(symptoms)

df["text"] = df.apply(combine_symptoms, axis=1)

df = df[["text", "Disease"]]
df.rename(columns={"Disease": "disease"}, inplace=True)

# Encode labels
le = LabelEncoder()
df["label"] = le.fit_transform(df["disease"])

print(df.head())

"""**TF-IDF Vectorization**"""

vectorizer = TfidfVectorizer()
X = vectorizer.fit_transform(df["text"])
y = df["label"]

"""**Train model**"""

clf = LogisticRegression(max_iter=1000)
clf.fit(X, y)



